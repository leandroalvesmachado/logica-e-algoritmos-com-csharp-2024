// Variáveis
int numero1 = 30;
int numero2 = 3;

// Divisão
Console.WriteLine($"Divisão: {numero1 / numero2}"); // Divisão: 10

// Multiplicação
Console.WriteLine($"Multiplicação: {numero1 * numero2}"); // Multiplicação: 90

// Subtração
Console.WriteLine($"Subtração: {numero1 - numero2}"); // Subtração: 27

// Soma
Console.WriteLine($"Soma: {numero1 + numero2}"); // Soma: 33

// Módulo (resto da divisão)
Console.WriteLine($"Módulo (resto da divisão): {numero1 % numero2}"); // Módulo (resto da divisão): 0

// Incrementador
numero1++;
Console.WriteLine(numero1); // 31

// Decrementador
numero2--;
Console.WriteLine(numero2); // 2
