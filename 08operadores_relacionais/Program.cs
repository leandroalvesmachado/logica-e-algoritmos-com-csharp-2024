// Variáveis
int numero1 = 5;
int numero2 = 8;

// Operadores relacionais servem para comparação
// True ou False

// Maior
Console.WriteLine(numero1 > numero2); // False

// Maior ou igual
Console.WriteLine(numero1 >= numero2); // False

// Menor
Console.WriteLine(numero1 < numero2); // True

// Menor ou igual
Console.WriteLine(numero1 <= numero2); // True

// Igual
Console.WriteLine(numero1 == numero2); // False

// Diferente
Console.WriteLine(numero1 != numero2); // True