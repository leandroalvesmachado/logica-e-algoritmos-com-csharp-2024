// Criando variável
string nome = "Leandro";

// Exibindo valor da variável
Console.WriteLine(nome);

// Alterando valor da variável
nome = "Leandro Alterado";

// Exibindo novo valor
Console.WriteLine(nome);


// criando uma constante
const string constante = "Leandro Constante";

// Erro, pois não é possível alterar o valor de uma constante
constante = "Leandro Constante alterado";

Console.WriteLine(constante);
